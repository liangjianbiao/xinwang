// 创建学校卡片HTML
function createSchoolCard(school, isMiddle = false) {
    const rankClass = school.rank === 1 ? 'top-1' : school.rank === 2 ? 'top-2' : school.rank === 3 ? 'top-3' : '';
    const scoreHtml = school.score ? `<span class="school-score">中考录取分数线: ${school.score}分</span>` : '';
    
    let tagsHtml = '';
    if (school.tags && school.tags.length > 0) {
        tagsHtml = '<div class="school-tags">';
        school.tags.forEach(tag => {
            const tagText = tag === 'featured' ? '重点学校' : tag === 'new' ? '新建学校' : '';
            const tagClass = tag === 'featured' ? 'featured' : tag === 'new' ? 'new' : '';
            if (tagText) {
                tagsHtml += `<span class="tag ${tagClass}">${tagText}</span>`;
            }
        });
        tagsHtml += '</div>';
    }

    let featuresHtml = '';
    if (school.features && school.features.length > 0) {
        featuresHtml = school.features.map(f => `<span class="tag">${f}</span>`).join('');
    }

    return `
        <div class="school-card">
            <div class="school-header">
                <div class="school-name">${school.name}</div>
                ${isMiddle && school.rank ? `<span class="school-rank ${rankClass}">排名第${school.rank}</span>` : ''}
            </div>
            ${scoreHtml}
            <div class="school-info">
                <div class="info-item">
                    <strong>📍 地址:</strong>
                    <span>${school.address}</span>
                </div>
                ${featuresHtml ? `<div class="school-tags">${featuresHtml}</div>` : ''}
            </div>
            ${tagsHtml}
        </div>
    `;
}

// 渲染学校列表
function renderSchools() {
    // 东南部学区
    document.getElementById('southeast-middle').innerHTML = 
        schoolData.southeast.middle.map(s => createSchoolCard(s, true)).join('');
    document.getElementById('southeast-primary').innerHTML = 
        schoolData.southeast.primary.map(s => createSchoolCard(s, false)).join('');

    // 中部学区
    document.getElementById('central-middle').innerHTML = 
        schoolData.central.middle.map(s => createSchoolCard(s, true)).join('');
    document.getElementById('central-primary').innerHTML = 
        schoolData.central.primary.map(s => createSchoolCard(s, false)).join('');

    // 北部学区
    document.getElementById('north-middle').innerHTML = 
        schoolData.north.middle.map(s => createSchoolCard(s, true)).join('');
    document.getElementById('north-primary').innerHTML = 
        schoolData.north.primary.map(s => createSchoolCard(s, false)).join('');

    // 西部学区
    document.getElementById('west-middle').innerHTML = 
        schoolData.west.middle.map(s => createSchoolCard(s, true)).join('');
    document.getElementById('west-primary').innerHTML = 
        schoolData.west.primary.map(s => createSchoolCard(s, false)).join('');

    // 更新统计
    updateStatistics();
}

// 更新统计数据
function updateStatistics() {
    let totalMiddle = 0;
    let totalPrimary = 0;

    Object.keys(schoolData).forEach(area => {
        totalMiddle += schoolData[area].middle.length;
        totalPrimary += schoolData[area].primary.length;
    });

    document.getElementById('total-schools').textContent = totalMiddle + totalPrimary;
    document.getElementById('total-middle').textContent = totalMiddle;
    document.getElementById('total-primary').textContent = totalPrimary;
}

// 标签页切换功能
function initTabs() {
    const tabBtns = document.querySelectorAll('.tab-btn');
    const sections = document.querySelectorAll('.school-section');

    tabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const area = btn.getAttribute('data-area');

            // 移除所有active类
            tabBtns.forEach(b => b.classList.remove('active'));
            sections.forEach(s => s.classList.remove('active'));

            // 添加active类
            btn.classList.add('active');
            document.getElementById(area).classList.add('active');
        });
    });
}

// 初始化
document.addEventListener('DOMContentLoaded', () => {
    renderSchools();
    initTabs();
});
